package c.example.speechtobsl.structure_converter.utils;

/**
 * The enum POS - tags to label words in the sentence.
 */
public enum POS {
        /**
         * A noun.
         */
        NOUN,
        /**
         * A verb.
         */
        VRB,
        /**
         * A determiner.
         */
        DET,
        /**
         * An adjective.
         */
        ADJ,
        /**
         * An adverb.
         */
        ADV,
        /**
         * A connective.
         */
        CONN,
        /**
         * A word to indicate time.
         */
        TIM,
        /**
         * A modal verb.
         */
        MDL,
        /**
         * A question word.
         */
        QTN,
        /**
         * A preposition.
         */
        PREP,
        /**
         * A word to be ignored.
         */
        NA
}
